package com.nt.vo;

public class PlayerVO {
	private String pName;
	private String team;
	private String runScored;
	private String timesOut;
	private String runsConceded;
	private String wicketTaken;
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getRunScored() {
		return runScored;
	}
	public void setRunScored(String runScored) {
		this.runScored = runScored;
	}
	public String getTimesOut() {
		return timesOut;
	}
	public void setTimesOut(String timesOut) {
		this.timesOut = timesOut;
	}
	public String getRunsConceded() {
		return runsConceded;
	}
	public void setRunsConceded(String runsConceded) {
		this.runsConceded = runsConceded;
	}
	public String getWicketTaken() {
		return wicketTaken;
	}
	public void setWicketTaken(String wicketTaken) {
		this.wicketTaken = wicketTaken;
	}
	
	

}
